type PathImpl<T, K extends keyof T> =
    K extends string
    ? T[K] extends Record<string, any>
        ? `${K}.${PathImpl<T[K], Exclude<keyof T[K], keyof any[]>> & string}` | `${K}.${Exclude<keyof T[K], keyof any[]> & string}`
        : `${K}`
    : never;

type Path<T> = PathImpl<T, keyof T> | keyof T;

export type TranslationKey = Path<typeof translations.en>;

// Helper function to replace placeholders like {name}
export function translate(text: string, replacements: Record<string, string> = {}): string {
    return Object.entries(replacements).reduce(
        (acc, [key, value]) => acc.replace(`{${key}}`, value),
        text
    );
}

    